// Utilize o for para imprimir os elementos da lista listaDeCompras com o console.log():

let listaDeCompras = ['Arroz', 'Feijão', 'Alface', 'Melancia'];

for (let index = 0; index < listaDeCompras.length; index++) {
  const produto = listaDeCompras[index];
  console.log(produto);
}